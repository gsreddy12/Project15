#islower()

str1="PYTHON"
print(str1.islower())
str2="sateesh"    #consider only ALL small letters and combination of sepecial operators and digits too
print(str2.islower())
str3="hi PYTHON"
print(str3.islower())
str4="123PYTHON"
print(str4.islower())
str5="hi python_%&^$#"
print(str5.islower())
str6="l^%$#%^"
print(str6.islower())
str7="^%$l#%^"
print(str7.islower())

